package com.left8.evs.samodule;

import java.io.BufferedReader;
import java.io.FileNotFoundException;
import java.io.FileReader;
import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Paths;
import java.util.ArrayList;
import java.util.List;
import java.util.logging.Level;
import java.util.logging.Logger;

import com.left8.evs.utilities.dsretriever.MongoHandler;
import com.left8.evs.utilities.dsretriever.Tweet;
import com.left8.evs.utilities.Config;
import com.left8.evs.utilities.PrintUtilities;

/**
 *
 * @author  Lefteris Paraskevas
 * @version 2016.11.26_1308  
 */
public class SentimentAnnotator {
    
    private final Config config;
    private final List<Tweet> tweets;
    private final MongoHandler mongo;
    private final List<Integer> sentimenAnnotations = new ArrayList<>();
    
    public SentimentAnnotator(Config config, List<Tweet> tweets, MongoHandler mongo) {
        this.config = config;
        this.tweets = new ArrayList<>(tweets);
        this.mongo = mongo;
        loadSentimentAnnotations();
    }
    
    /**
     * Method that parses output files generated by Weka and stores the information
     * in the MongoDB Store.
     */
    private void loadSentimentAnnotations() {
        try {
            Files.walk(Paths.get(config.getResourcesPath() 
                    + config.getSentimentFilesPath())).forEach(filePath -> {
                if(Files.isRegularFile(filePath)) {
                    PrintUtilities.printInfoMessageln("Reading " + filePath.getFileName() + " file...");
                    String line;
                    String[] temp;
                    try {
                        BufferedReader in = new BufferedReader(new FileReader(filePath.toString()));
                        
                        boolean firstLine = true;
                        while((line = in.readLine()) != null) {
                            if(firstLine) {
                                firstLine = false;
                                continue;
                            }
                            line = line.replaceAll("        ", " ");
                            line = line.replaceAll("       ", " ");
                            line = line.replaceAll("      ", " ");
                            line = line.replaceAll("     ", " ");
                            line = line.replaceAll("    ", " ");
                            line = line.replaceAll("   ", " ");
                            line = line.replaceAll("  ", " ");
                            temp = line.split(" ");
                            sentimenAnnotations.add(Integer.parseInt(temp[3].substring(2)));
                        }
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(SentimentAnnotator.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (IOException ex) {
                        Logger.getLogger(SentimentAnnotator.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            });
        } catch (IOException ex) {
            Logger.getLogger(SentimentAnnotator.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    /**
     * Annotates a list of tweets with their sentiment which was already generated
     * by the Naive Bayes classifier.
     */
    public final void annotateWithNaiveBayesSentiment() {
        int i = 0;
        for(Tweet tweet : tweets) {
            mongo.updateSentiment(tweet.getID(), sentimenAnnotations.get(i),
                    config.getNaiveBayesSentimentName());
            i++;
        }
    }
    
    /**
     * Annotates a list of tweets with their sentiment which was already generated
     * by the Bayesian Net classifier.
     */
    public final void annotateWithBayesianNetSentiment() {
        int i = 0;
        for(Tweet tweet : tweets) {
            mongo.updateSentiment(tweet.getID(), sentimenAnnotations.get(i),
                    config.getBayesianNetSentimentName());
            i++;
        }
    }
}
